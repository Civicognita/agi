describe("OutboundDispatcher", () => {
  let mockCOA: ReturnType<typeof createMockCOALogger>;
  let plugin: ReturnType<typeof createMockPlugin>;
  let dispatcher: OutboundDispatcher;

  // A fixed entity ID string — OutboundDispatcher only passes it to coaLogger;
  // with the mock logger there is no entity format constraint.
  const TEST_ENTITY_ID = "entity-out-test";

  beforeEach(() => {
    mockCOA = createMockCOALogger();
    plugin = createMockPlugin("telegram");

    dispatcher = new OutboundDispatcher({
      getChannelAdapter: (id) => id === "telegram" ? plugin.outbound : undefined,
      coaLogger: mockCOA.logger,
      resolveCoaAlias: (entityId) => `#MOCK-${entityId}`,
      resourceId: "$A0",
      nodeId: "@A0",
    });
  });

  it("dispatches a single message to the correct channel adapter", async () => {
    const route: OutboundRoute = {
      channelId: "telegram",
      channelUserId: "user-001",
      content: { type: "text", text: "Hello!" },
      entityId: TEST_ENTITY_ID,
    };

    const result = await dispatcher.dispatch(route);
    expect(result.channelId).toBe("telegram");
    expect(result.channelUserId).toBe("user-001");
    expect(plugin._test.sentMessages).toHaveLength(1);
    expect(plugin._test.sentMessages[0]!.userId).toBe("user-001");
  });

  it("throws if channel adapter not found", async () => {
    const route: OutboundRoute = {
      channelId: "discord",
      channelUserId: "user-001",
      content: { type: "text", text: "Hello!" },
      entityId: TEST_ENTITY_ID,
    };
    await expect(dispatcher.dispatch(route)).rejects.toThrow(/Channel not found/);
  });

  it("returns a delivered COA fingerprint placeholder", async () => {
    // COA entry is created at invocation DONE, not per outbound message.
    // The fingerprint is a placeholder: $resourceId.#alias.@nodeId.delivered
    const route: OutboundRoute = {
      channelId: "telegram",
      channelUserId: "user-001",
      content: { type: "text", text: "COA test" },
      entityId: TEST_ENTITY_ID,
    };

    const result = await dispatcher.dispatch(route);
    expect(result.coaFingerprint).toBeDefined();
    expect(result.coaFingerprint).toContain(".delivered");
    expect(result.coaFingerprint).toContain("$A0");
  });

  it("returns a deliveredAt ISO timestamp", async () => {
    const route: OutboundRoute = {
      channelId: "telegram",
      channelUserId: "user-001",
      content: { type: "text", text: "time test" },
      entityId: TEST_ENTITY_ID,
    };

    const result = await dispatcher.dispatch(route);
    expect(() => new Date(result.deliveredAt)).not.toThrow();
  });

  it("dispatchBatch processes multiple routes", async () => {
    const routes: OutboundRoute[] = [
      { channelId: "telegram", channelUserId: "u1", content: { type: "text", text: "msg1" }, entityId: TEST_ENTITY_ID },
      { channelId: "telegram", channelUserId: "u2", content: { type: "text", text: "msg2" }, entityId: TEST_ENTITY_ID },
    ];

    const batch = await dispatcher.dispatchBatch(routes);
    expect(batch.successCount).toBe(2);
    expect(batch.failureCount).toBe(0);
    expect(plugin._test.sentMessages).toHaveLength(2);
  });

  it("dispatchBatch reports individual failures without aborting", async () => {
    const routes: OutboundRoute[] = [
      { channelId: "telegram", channelUserId: "u1", content: { type: "text", text: "ok" }, entityId: TEST_ENTITY_ID },
      // discord is not registered — will fail
      { channelId: "discord", channelUserId: "u2", content: { type: "text", text: "fail" }, entityId: TEST_ENTITY_ID },
    ];

    const batch = await dispatcher.dispatchBatch(routes);
    expect(batch.successCount).toBe(1);
    expect(batch.failureCount).toBe(1);
    // telegram message was still sent
    expect(plugin._test.sentMessages).toHaveLength(1);
  });
});

