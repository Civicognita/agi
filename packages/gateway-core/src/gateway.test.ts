describe("InboundRouter", () => {
  let db: Database;
  let entityStore: EntityStore;
  let messageQueue: MessageQueue;
  let mockCOA: ReturnType<typeof createMockCOALogger>;
  let router: InboundRouter;

  beforeEach(() => {
    db = createTestDb();
    entityStore = new EntityStore(db);
    messageQueue = new MessageQueue(db);
    mockCOA = createMockCOALogger();
    router = new InboundRouter({
      entityStore,
      messageQueue,
      coaLogger: mockCOA.logger,
      resourceId: "$A0",
      nodeId: "@A0",
    });
  });

  it("routes a message and returns entityId, coaFingerprint, and queueMessageId", async () => {
    const msg = makeAionimaMessage("telegram", "user-001");
    const result = (await router.route(msg))!;
    expect(typeof result.entityId).toBe("string");
    expect(result.entityId.length).toBeGreaterThan(0);
    expect(typeof result.coaFingerprint).toBe("string");
    expect(result.coaFingerprint.length).toBeGreaterThan(0);
    expect(typeof result.queueMessageId).toBe("string");
    expect(result.queueMessageId.length).toBeGreaterThan(0);
  });

  it("creates a new entity if none exists for the channel user", async () => {
    const msg = makeAionimaMessage("telegram", "new-user");
    const result = (await router.route(msg))!;
    const entity = entityStore.getEntity(result.entityId);
    expect(entity).not.toBeNull();
    expect(entity!.type).toBe("E");
  });

  it("reuses existing entity on subsequent messages from same user", async () => {
    const msg1 = makeAionimaMessage("telegram", "user-001", "first");
    const msg2 = makeAionimaMessage("telegram", "user-001", "second");
    const r1 = (await router.route(msg1))!;
    const r2 = (await router.route(msg2))!;
    expect(r1.entityId).toBe(r2.entityId);
  });

  it("returns a pending COA fingerprint for deferred logging", async () => {
    // COA entry is NOT created at route time — it's created when the agent
    // COMPLETES its response (agent-invoker.ts). The fingerprint is a
    // placeholder in the format: $resourceId.#coaAlias.@nodeId.pending
    const msg = makeAionimaMessage("telegram", "user-coa");
    const result = (await router.route(msg))!;
    expect(result.coaFingerprint).toContain(".pending");
    expect(result.coaFingerprint).toContain("$A0");
    expect(result.coaFingerprint).toContain("@A0");
  });

  it("enqueues message as inbound in the message queue", async () => {
    const msg = makeAionimaMessage("telegram", "user-queue");
    const result = (await router.route(msg))!;
    // Queue depth should be 1 (message is pending)
    expect(messageQueue.depth("inbound")).toBe(1);
    // The enqueued message id matches
    const peeked = messageQueue.peek("inbound");
    expect(peeked[0]!.id).toBe(result.queueMessageId);
  });

  it("different channel users produce different entity IDs", async () => {
    const msg1 = makeAionimaMessage("telegram", "user-A");
    const msg2 = makeAionimaMessage("telegram", "user-B");
    const r1 = (await router.route(msg1))!;
    const r2 = (await router.route(msg2))!;
    expect(r1.entityId).not.toBe(r2.entityId);
  });

  it("COA fingerprint is a non-empty placeholder string", async () => {
    const msg = makeAionimaMessage("telegram", "user-fp");
    const result = (await router.route(msg))!;
    expect(result.coaFingerprint.length).toBeGreaterThan(0);
    // Fingerprint is a placeholder — actual COA record created at agent completion
    expect(result.coaFingerprint).toMatch(/\$A0\..*\.@A0\.pending/);
  });
});

