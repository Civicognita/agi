function createMockPlugin(id: string): AionimaChannelPlugin & { _test: { sentMessages: Array<{ userId: string; content: unknown }>; getMessageHandler: () => ((msg: AionimaChannelPlugin) => Promise<void>) | null; isRunning: () => boolean } } {
  let running = false;
  const sentMessages: Array<{ userId: string; content: unknown }> = [];
  let messageHandler: ((msg: unknown) => Promise<void>) | null = null;

  return {
    id: id as ChannelId,
    meta: { name: `Mock ${id}`, version: "1.0.0" },
    capabilities: { text: true, media: false, voice: false, reactions: false, threads: false, ephemeral: false },
    config: {
      validate: () => true,
      getDefaults: () => ({}),
    },
    gateway: {
      start: async () => { running = true; },
      stop: async () => { running = false; },
      isRunning: () => running,
    },
    outbound: {
      send: async (userId: string, content: unknown) => { sentMessages.push({ userId, content }); },
    },
    messaging: {
      onMessage: (handler: (msg: unknown) => Promise<void>) => { messageHandler = handler; },
    },
    // Expose internals for assertions
    _test: {
      sentMessages,
      getMessageHandler: () => messageHandler as ((msg: AionimaChannelPlugin) => Promise<void>) | null,
      isRunning: () => running,
    },
  } as unknown as AionimaChannelPlugin & { _test: { sentMessages: Array<{ userId: string; content: unknown }>; getMessageHandler: () => ((msg: AionimaChannelPlugin) => Promise<void>) | null; isRunning: () => boolean } };
}

