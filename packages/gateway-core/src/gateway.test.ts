function makeAionimaMessage(channelId: string, channelUserId: string, text = "hello") {
  return {
    id: `msg-${channelId}-${channelUserId}-${Date.now()}`,
    channelId: channelId as ChannelId,
    channelUserId,
    timestamp: new Date().toISOString(),
    content: { type: "text" as const, text },
  };
}

