describe("Gateway E2E", () => {
  // Uses the mock COA logger to avoid entity ID format constraints while
  // still testing the full routing pipeline end-to-end.
  let db: Database;
  let entityStore: EntityStore;
  let messageQueue: MessageQueue;
  let mockCOA: ReturnType<typeof createMockCOALogger>;
  let plugin: ReturnType<typeof createMockPlugin>;
  let router: InboundRouter;
  let dispatcher: OutboundDispatcher;

  beforeEach(() => {
    db = createTestDb();
    entityStore = new EntityStore(db);
    messageQueue = new MessageQueue(db);
    mockCOA = createMockCOALogger();
    plugin = createMockPlugin("telegram");

    router = new InboundRouter({
      entityStore,
      messageQueue,
      coaLogger: mockCOA.logger,
      resourceId: "$A0",
      nodeId: "@A0",
    });

    dispatcher = new OutboundDispatcher({
      getChannelAdapter: (id) => id === "telegram" ? plugin.outbound : undefined,
      coaLogger: mockCOA.logger,
      resolveCoaAlias: (entityId) => `#MOCK-${entityId}`,
      resourceId: "$A0",
      nodeId: "@A0",
    });
  });

  it("full inbound pipeline: InboundRouter creates entity + COA + enqueue, QueueConsumer delivers to onInbound", async () => {
    const receivedMessages: QueueMessage[] = [];

    const consumer = new QueueConsumer(
      {
        messageQueue,
        outboundDispatcher: dispatcher,
        onInbound: async (msg) => { receivedMessages.push(msg); },
      },
      { pollIntervalMs: 50, concurrency: 5 },
    );

    // Simulate inbound message from channel adapter
    const msg = makeAionimaMessage("telegram", "user-e2e", "Hello, Aionima!");
    const routeResult = (await router.route(msg))!;

    // Verify entity was created in the store
    const entity = entityStore.getEntity(routeResult.entityId);
    expect(entity).not.toBeNull();

    // COA is deferred to agent completion — verify placeholder fingerprint
    expect(routeResult.coaFingerprint).toContain(".pending");

    // Verify message was enqueued
    expect(messageQueue.depth("inbound")).toBe(1);

    // Start consumer and let it drain
    consumer.start();
    await new Promise((r) => setTimeout(r, 200));
    await consumer.stop();

    // onInbound should have been called
    expect(receivedMessages).toHaveLength(1);
    const payload = receivedMessages[0]!.payload as { entityId: string; coaFingerprint: string };
    expect(payload.entityId).toBe(routeResult.entityId);
    expect(payload.coaFingerprint).toBe(routeResult.coaFingerprint);
  });

  it("outbound pipeline: enqueue outbound → QueueConsumer dequeues → OutboundDispatcher → adapter.send called", async () => {
    const consumer = new QueueConsumer(
      {
        messageQueue,
        outboundDispatcher: dispatcher,
        onInbound: async () => {},
      },
      { pollIntervalMs: 50, concurrency: 5 },
    );

    // Enqueue an outbound route directly — mock COA accepts any entity ID
    const outboundRoute: OutboundRoute = {
      channelId: "telegram",
      channelUserId: "user-e2e-out",
      content: { type: "text", text: "Outbound E2E message" },
      entityId: "e2e-entity-out",
    };

    messageQueue.enqueue({ channel: "telegram", direction: "outbound", payload: outboundRoute });

    // Start consumer and drain
    consumer.start();
    await new Promise((r) => setTimeout(r, 200));
    await consumer.stop();

    // Adapter send should have been called
    expect(plugin._test.sentMessages).toHaveLength(1);
    expect(plugin._test.sentMessages[0]!.userId).toBe("user-e2e-out");
    expect((plugin._test.sentMessages[0]!.content as { type: string; text: string }).text).toBe("Outbound E2E message");
  });

  it("bidirectional pipeline: inbound triggers entity creation, subsequent outbound reuses entity", async () => {
    const inboundMsg = makeAionimaMessage("telegram", "user-bidir", "ping");
    const routeResult = (await router.route(inboundMsg))!;

    // Verify entity was created
    const entity = entityStore.getEntity(routeResult.entityId);
    expect(entity).not.toBeNull();

    // Send outbound to same entity (mock COA logs it without format constraint)
    const outboundRoute: OutboundRoute = {
      channelId: "telegram",
      channelUserId: "user-bidir",
      content: { type: "text", text: "pong" },
      entityId: routeResult.entityId,
    };

    const result = await dispatcher.dispatch(outboundRoute);
    expect(result.channelUserId).toBe("user-bidir");
    expect(plugin._test.sentMessages[0]!.userId).toBe("user-bidir");

    // COA is deferred — both inbound and outbound return placeholder fingerprints
    expect(routeResult.coaFingerprint).toContain(".pending");
    expect(result.coaFingerprint).toContain(".delivered");
  });

  it("multiple users produce isolated entity records", async () => {
    const msg1 = makeAionimaMessage("telegram", "user-alpha");
    const msg2 = makeAionimaMessage("telegram", "user-beta");

    const r1 = (await router.route(msg1))!;
    const r2 = (await router.route(msg2))!;

    expect(r1.entityId).not.toBe(r2.entityId);

    const entities = entityStore.listEntities();
    expect(entities).toHaveLength(2);
  });
});
