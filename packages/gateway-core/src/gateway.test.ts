describe("ChannelRegistry", () => {
  let registry: ChannelRegistry;

  beforeEach(() => {
    registry = new ChannelRegistry();
  });

  it("registers a valid plugin without throwing", () => {
    const plugin = createMockPlugin("telegram");
    expect(() => registry.register(plugin)).not.toThrow();
  });

  it("getChannel returns the entry after registration", () => {
    const plugin = createMockPlugin("telegram");
    registry.register(plugin);
    const entry = registry.getChannel("telegram");
    expect(entry).toBeDefined();
    expect(entry!.plugin.id).toBe("telegram");
    expect(entry!.status).toBe("registered");
  });

  it("throws on duplicate registration of the same id", () => {
    registry.register(createMockPlugin("telegram"));
    expect(() => registry.register(createMockPlugin("telegram"))).toThrow(/already registered/);
  });

  it("throws on invalid adapter (missing required adapters)", () => {
    const invalid = { id: "bad" as ChannelId } as AionimaChannelPlugin;
    expect(() => registry.register(invalid)).toThrow();
  });

  it("starts a channel and transitions status to running", async () => {
    const plugin = createMockPlugin("telegram");
    registry.register(plugin);
    await registry.startChannel("telegram");
    expect(registry.getChannel("telegram")!.status).toBe("running");
    expect(plugin._test.isRunning()).toBe(true);
  });

  it("stops a running channel and transitions status to stopped", async () => {
    const plugin = createMockPlugin("telegram");
    registry.register(plugin);
    await registry.startChannel("telegram");
    await registry.stopChannel("telegram");
    expect(registry.getChannel("telegram")!.status).toBe("stopped");
    expect(plugin._test.isRunning()).toBe(false);
  });

  it("emits channel_registered event on registration", () => {
    const events: string[] = [];
    registry.on("channel_registered", (id) => events.push(id));
    registry.register(createMockPlugin("telegram"));
    expect(events).toEqual(["telegram"]);
  });

  it("emits channel_started event after successful start", async () => {
    const events: string[] = [];
    registry.on("channel_started", (id) => events.push(id));
    registry.register(createMockPlugin("telegram"));
    await registry.startChannel("telegram");
    expect(events).toEqual(["telegram"]);
  });

  it("emits channel_stopped event after successful stop", async () => {
    const events: string[] = [];
    registry.on("channel_stopped", (id) => events.push(id));
    registry.register(createMockPlugin("telegram"));
    await registry.startChannel("telegram");
    await registry.stopChannel("telegram");
    expect(events).toEqual(["telegram"]);
  });

  it("startChannel throws when channel id is not registered", async () => {
    await expect(registry.startChannel("nonexistent")).rejects.toThrow(/not registered/);
  });

  it("stopChannel throws when channel id is not registered", async () => {
    await expect(registry.stopChannel("nonexistent")).rejects.toThrow(/not registered/);
  });

  it("startAll starts all registered channels in parallel", async () => {
    const p1 = createMockPlugin("ch1");
    const p2 = createMockPlugin("ch2");
    registry.register(p1);
    registry.register(p2);
    await registry.startAll();
    expect(p1._test.isRunning()).toBe(true);
    expect(p2._test.isRunning()).toBe(true);
  });

  it("stopAll stops only running channels", async () => {
    const p1 = createMockPlugin("ch1");
    const p2 = createMockPlugin("ch2");
    registry.register(p1);
    registry.register(p2);
    // Only start ch1
    await registry.startChannel("ch1");
    await registry.stopAll();
    expect(p1._test.isRunning()).toBe(false);
    // ch2 was never started — its stop should not have been called
    expect(p2._test.isRunning()).toBe(false);
    expect(registry.getChannel("ch1")!.status).toBe("stopped");
    expect(registry.getChannel("ch2")!.status).toBe("registered");
  });

  it("unregister removes channel from registry", () => {
    registry.register(createMockPlugin("telegram"));
    registry.unregister("telegram");
    expect(registry.getChannel("telegram")).toBeUndefined();
  });

  it("unregister emits channel_unregistered", () => {
    const events: string[] = [];
    registry.on("channel_unregistered", (id) => events.push(id));
    registry.register(createMockPlugin("telegram"));
    registry.unregister("telegram");
    expect(events).toEqual(["telegram"]);
  });

  it("unregister is a no-op for unknown id", () => {
    expect(() => registry.unregister("nonexistent")).not.toThrow();
  });

  it("getChannels returns all registered entries", () => {
    registry.register(createMockPlugin("ch1"));
    registry.register(createMockPlugin("ch2"));
    expect(registry.getChannels()).toHaveLength(2);
  });

  it("getRunningChannels returns only running entries", async () => {
    registry.register(createMockPlugin("ch1"));
    registry.register(createMockPlugin("ch2"));
    await registry.startChannel("ch1");
    const running = registry.getRunningChannels();
    expect(running).toHaveLength(1);
    expect(running[0]!.plugin.id).toBe("ch1");
  });

  it("registeredAt is an ISO timestamp", () => {
    const before = new Date().toISOString();
    registry.register(createMockPlugin("telegram"));
    const after = new Date().toISOString();
    const entry = registry.getChannel("telegram")!;
    expect(entry.registeredAt >= before).toBe(true);
    expect(entry.registeredAt <= after).toBe(true);
  });
});

