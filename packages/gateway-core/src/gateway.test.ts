// ---------------------------------------------------------------------------
// Mock COA logger
//
// COAChainLogger.log() validates entity IDs as COA notation (#E0, #O0, etc.)
// which is incompatible with the ULID IDs generated by EntityStore. For
// InboundRouter and OutboundDispatcher tests, we use a lightweight stub that
// accepts any entity ID and returns deterministic fingerprints.
// ---------------------------------------------------------------------------

interface MockCOARecord {
  resourceId: string;
  entityId: string;
  nodeId: string;
  workType: string;
  fingerprint: string;
}

function createMockCOALogger(): { logger: COAChainLogger; records: MockCOARecord[] } {
  const records: MockCOARecord[] = [];
  let counter = 0;

  const logger = {
    log: (params: { resourceId: string; entityId: string; nodeId: string; workType: string; ref?: string }) => {
      counter++;
      const fingerprint = `${params.resourceId}.ENTITY${counter}.${params.nodeId}.C${String(counter).padStart(3, "0")}`;
      records.push({ ...params, fingerprint });
      return fingerprint;
    },
    getRecord: (fp: string) => records.find((r) => r.fingerprint === fp) ?? null,
    getChain: (entityId: string) => records.filter((r) => r.entityId === entityId),
    getLatestCounter: () => counter,
  } as unknown as COAChainLogger;

  return { logger, records };
}

